
function nombreArtista(){

    let nombreIngresado=prompt("Ingresa el nombre de tu artista")
    if(nombreIngresado=="Los Tipitos"){
        alert ("Ya tienes tu entrada")
    }else if (nombreIngresado=="Mana"){
        alert ("Funcion cancelada")
    }else{
        alert ("Entradas agotadas")
    }    
}
nombreArtista();

